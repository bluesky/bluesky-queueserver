import pytest

from bluesky_queueserver.manager.utils import filter_dict_by_permitted_keys


@pytest.mark.parametrize(
    "d, permitted_keys, expected",
    [
        ({"a": 1, "b": 2, "c": 3}, ["/"], {"a": 1, "b": 2, "c": 3}),
        ({"a": 1, "b": 2, "c": 3}, ["/a"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/a/"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/b"], {"b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/b/"], {"b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/d"], {}),
        ({"a": 1, "b": 2, "c": 3}, ["/d/"], {}),
        ({"a": 1, "b": 2, "c": 3}, ["/a", "/b"], {"a": 1, "b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/a/", "/b/"], {"a": 1, "b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/a", "/b/"], {"a": 1, "b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/a/", "/b"], {"a": 1, "b": 2}),
        ({"a": 1, "b": 2, "c": 3}, ["/a", "/d"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/a/", "/d/"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/a", "/d/"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/a/", "/d"], {"a": 1}),
        ({"a": 1, "b": 2, "c": 3}, ["/", "/a/", "/d"], {"a": 1, "b": 2, "c": 3}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/"], {"a": {"b": 1, "c": 2}, "d": 2}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a"], {"a": {"b": 1, "c": 2}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/"], {"a": {"b": 1, "c": 2}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/b"], {"a": {"b": 1}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/b/"], {"a": {"b": 1}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/c"], {"a": {"c": 2}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/c/"], {"a": {"c": 2}}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/d"], {}),
        ({"a": {"b": 1, "c": 2}, "d": 2}, ["/a/d/"], {}),
    ],
)
def test_filter_dict_by_permitted_keys(d, permitted_keys, expected):
    assert filter_dict_by_permitted_keys(d, permitted_keys) == expected
